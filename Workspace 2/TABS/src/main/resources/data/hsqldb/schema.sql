

create table Account (
	id BIGINT GENERATED by default as IDENTITY (START with 1, INCREMENT by 1) NOT NULL,
	email varchar(64),
	username varchar(100),
	password varchar(64),
	student_id int,
	token int,
	primary key (id)
);

create table Activity (
	id BIGINT GENERATED by default as IDENTITY (START with 1, INCREMENT by 1) NOT NULL,
	activity_id int,
	title varchar(100),
	location varchar(100),
	seat int,
	startDate date,
	startTime time,
	status VARCHAR(10),
	primary key (id)
);


